class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            DiceRollerApp()
        }
    }
}

@Preview
@Composable
fun DiceRollerApp() {
    DiceWithButtonAndImage()
}

@Composable
fun DiceWithButtonAndImage(modifier: Modifier = Modifier) {
    var result by remember { mutableStateOf(1) }
    Column(
        modifier = modifier
            .fillMaxSize(), // Ocupa todo o espaço disponível
        verticalArrangement = Arrangement.Center,
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        Spacer(modifier = Modifier.weight(1f))
        Image(
            painter = painterResource(getDiceImage(result)),
            contentDescription = result.toString(),
            modifier = Modifier
                .height(200.dp) // Ajuste a altura conforme necessário
        )
        Spacer(modifier = Modifier.height(16.dp))
        Button(onClick = { result = Random.nextInt(1, 7) }) {
            Text(stringResource(R.string.roll))
        }
        Spacer(modifier = Modifier.weight(1f))
    }
}

private fun getDiceImage(result: Int): Int {
    return when (result) {
        1 -> R.drawable.dice_1
        2 -> R.drawable.dice_2
        3 -> R.drawable.dice_3
        4 -> R.drawable.dice_4
        5 -> R.drawable.dice_5
        else -> R.drawable.dice_6
    }
}
